          <button class="cookie-accept" @click="acceptCookies">Akzeptieren</button>
          <button class="cookie-reject" @click="rejectCookies">Ablehnen</button>
        </div>
      </div>
    </div>
  </div>
</template>

<script>
export default {
  data() {
    return {
      currentPage: 'home',
      menuOpen: false,
      showImpressumModal: false,
      showDatenschutzModal: false,
      showCookieBanner: false,
      form: {
        name: '',
        email: '',
        company: '',
        phone: '',
        message: ''
      },
      formMessage: '',
      formMessageType: ''
    };
  },
  mounted() {
    this.showCookieBanner = !localStorage.getItem('cookieConsent');
  },
  methods: {
    toggleMenu() {
      this.menuOpen = !this.menuOpen;
    },
    closeMenu() {
      this.menuOpen = false;
    },
    scrollToServices() {
      this.closeMenu();
      setTimeout(() => {
        document.getElementById('services').scrollIntoView({ behavior: 'smooth' });
      }, 0);
    },
    scrollToContact() {
      this.closeMenu();
      setTimeout(() => {
        document.getElementById('contact').scrollIntoView({ behavior: 'smooth' });
      }, 0);
    },
    showImpressum() {
      this.showImpressumModal = true;
      this.closeMenu();
    },
    showDatenschutz() {
      this.showDatenschutzModal = true;
      this.closeMenu();
    },
    showAGB() {
      this.currentPage = 'agb';
      window.scrollTo(0, 0);
    },
    goHome() {
      this.currentPage = 'home';
      window.scrollTo(0, 0);
    },
    acceptCookies() {
      localStorage.setItem('cookieConsent', 'true');
      this.showCookieBanner = false;
    },
    rejectCookies() {
      localStorage.setItem('cookieConsent', 'false');
      this.showCookieBanner = false;
    },
    async submitForm() {
      if (!localStorage.getItem('cookieConsent')) {
        this.formMessage = 'Bitte akzeptieren Sie die Cookies, um das Formular zu senden.';
        this.formMessageType = 'error';
        return;
      }

      try {
        const response = await fetch('https://formspree.io/f/xldorvea', {
          method: 'POST',
          headers: {
            'Content-Type': 'application/json'
          },
          body: JSON.stringify(this.form)
        });

        if (response.ok) {
          this.formMessage = 'Vielen Dank! Wir melden uns in K√ºrze bei Ihnen.';
          this.formMessageType = 'success';
          this.form = { name: '', email: '', company: '', phone: '', message: '' };
          setTimeout(() => {
            this.formMessage = '';
          }, 5000);
        } else {
          this.formMessage = 'Fehler beim Senden. Bitte versuchen Sie es sp√§ter erneut.';
          this.formMessageType = 'error';
        }
      } catch (error) {
        this.formMessage = 'Fehler beim Senden. Bitte versuchen Sie es sp√§ter erneut.';
        this.formMessageType = 'error';
      }
    }
  }
};
</script>
